#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t pos;
	size_t dest_size;

	dest_size = ft_strlen(dest);
	pos = 0;
	while ((src[pos] != '\0') && (pos < n))
	{
		dest[dest_size + pos] = src[pos];
		pos++;
	}
	dest[dest_size + pos] = '\0';
	return (dest);
}
