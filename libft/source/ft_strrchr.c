#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	size_t			pos;

	pos = ft_strlen(str);
	if (c == 0)
		return ((char *)(str + pos));
	while (pos > 0)
	{
		if (str[--pos] == c)
			return ((char *)(str + pos));
	}
	return (NULL);
}
