#include "libft.h"

void	ft_putnbr(int n)
{
	char c;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			n *= -1;
			ft_putchar('-');
		}
		if (n / 10 != 0)
			ft_putnbr(n / 10);
		c = n % 10 + '0';
		ft_putchar(c);
	}
}
