#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;
	size_t			pos;

	p = (unsigned char*)s;
	pos = 0;
	while (pos < n)
	{
		p[pos] = 0;
		pos++;
	}
}
