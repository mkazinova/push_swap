#include "libft.h"

char	*ft_strtrim(char const *s)
{
	char	*ans;
	size_t	i;
	size_t	j;

	if (s == NULL)
		return (NULL);
	i = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i++;
	j = ft_strlen(s);
	if (i == j)
		return ("");
	while (s[j - 1] == ' ' || s[j - 1] == '\n' || s[j - 1] == '\t')
		j--;
	if (!(ans = ft_strsub(s, i, j - i)))
		return (NULL);
	return (ans);
}
