#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	*ptr;
	size_t			pos;

	ptr = (unsigned char*)str;
	pos = 0;
	while (pos < n)
	{
		ptr[pos] = c;
		pos++;
	}
	return (str);
}
