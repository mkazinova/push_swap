#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t len)
{
	size_t pos;

	pos = 0;
	while ((s1[pos] == s2[pos]) && s1[pos] && s2[pos] && pos < len)
		pos++;
	if (pos == len)
		return (0);
	return ((unsigned char)(s1[pos]) - (unsigned char)(s2[pos]));
}
