#include "libft.h"

void	ft_putendl(char const *s)
{
	int pos;

	if (s != NULL)
	{
		pos = 0;
		while (s[pos] != '\0')
		{
			ft_putchar(s[pos]);
			pos++;
		}
		ft_putchar('\n');
	}
}
