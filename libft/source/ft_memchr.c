#include "libft.h"

void	*ft_memchr(const void *ptr, int c, size_t num)
{
	unsigned char	*str;
	size_t			pos;

	str = (unsigned char*)ptr;
	pos = 0;
	while (pos < num)
	{
		if (str[pos] == (unsigned char)c)
			return ((void*)(str + pos));
		pos++;
	}
	return (NULL);
}
