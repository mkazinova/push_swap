#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	size_t			pos;

	pos = 0;
	while (str[pos] != '\0')
	{
		if (str[pos] == (char)c)
			return ((char *)(str + pos));
		pos++;
	}
	if (c == 0)
		return ((char *)(str + pos));
	return (NULL);
}
