#include "libft.h"

char	*ft_strnew(size_t size)
{
	void	*ptr;

	ptr = (char *)malloc(size + 1);
	if (ptr == NULL)
	{
		return (NULL);
	}
	ft_bzero(ptr, size + 1);
	return (ptr);
}
